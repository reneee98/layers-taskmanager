// Helper funkcie na prácu s mock dátami
export const getMockClients = () => mockClients;
export const getMockProjects = () => mockProjects;
export const getMockTasks = () => mockTasks;

export const getMockClientById = (id: string) => mockClients.find((c) => c.id === id);
export const getMockProjectById = (id: string) => mockProjects.find((p) => p.id === id);
export const getMockTaskById = (id: string) => mockTasks.find((t) => t.id === id);

export const getMockProjectsByClient = (clientId: string) =>
  mockProjects.filter((p) => p.client_id === clientId);

export const getMockTasksByProject = (projectId: string) =>
  mockTasks.filter((t) => t.project_id === projectId && !t.parent_task_id);

export const getMockSubTasks = (parentTaskId: string) =>
  mockTasks.filter((t) => t.parent_task_id === parentTaskId);

